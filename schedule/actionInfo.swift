import Foundation


class actionInfo {

    var mVolume:Int
    var vibrate:Bool
    var nVolume:Int
    var name:String
    var hourOfDay:Int
    var minuteOfHour:Int
    var daysOfWeek:Int
    
    
    
    
    
    init(mVolume a:Int, nVolume b:Int, vibrate c:Bool, name d:String, hourOfDay e:Int, minuteOfHour f: Int, daysOfWeek g:Int){
        
        mVolume = a
        nVolume = b
        vibrate = c
        name = d
        hourOfDay = e
        minuteOfHour = f
        daysOfWeek = g
        
        
        
    }


















}
